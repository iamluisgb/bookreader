// i18n (P15): inglés/español sin build ni dependencias, estilo gettext.
// La CLAVE es la cadena española original (fuente); el diccionario EN traduce.
// Sin traducción → cae al español (nunca se muestra una key rota).
// Idioma: localStorage 'bookreader_lang' ('en'|'es'); primera vez, navigator.language.
// Interpolación con {x}: t('Página {n}', { n: 3 }).
// HTML estático: marcar con data-i18n (textContent) y/o data-i18n-attrs="aria-label,title"
// y llamar a translateDom() en el arranque (usa el valor español actual como clave).

const LANG_KEY = 'bookreader_lang';
let lang = null;

export function getLang() {
  if (lang) return lang;
  let saved = null;
  try { saved = localStorage.getItem(LANG_KEY); } catch (e) { /* storage bloqueado */ }
  if (saved === 'es' || saved === 'en') lang = saved;
  else lang = /^es(-|_|$)/i.test(navigator.language || '') ? 'es' : 'en';
  return lang;
}

export function setLang(l) {
  if (l !== 'es' && l !== 'en') return;
  try { localStorage.setItem(LANG_KEY, l); } catch (e) { /* storage bloqueado */ }
  lang = l;
}

// Nombre del idioma de la UI para las directivas de los prompts del agente
// ("responde en el idioma del usuario; por defecto, <este>"). Ver P15 en BACKLOG.
export function uiLangName() {
  return getLang() === 'en' ? 'inglés (English)' : 'español';
}

export function t(source, params) {
  let s = source;
  if (getLang() === 'en') {
    const hit = EN[source];
    if (hit !== undefined) s = hit;
  }
  if (params) for (const k of Object.keys(params)) s = s.split(`{${k}}`).join(String(params[k]));
  return s;
}

// Traduce el HTML estático ya presente en el DOM. Idempotente solo en español
// (en inglés reescribe el valor), así que se llama UNA vez en el arranque.
export function translateDom(root = document) {
  document.documentElement.lang = getLang();
  if (getLang() === 'es') return;
  root.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.textContent.trim());
  });
  root.querySelectorAll('[data-i18n-attrs]').forEach(el => {
    for (const raw of el.getAttribute('data-i18n-attrs').split(',')) {
      const a = raw.trim();
      const v = el.getAttribute(a);
      if (v) el.setAttribute(a, t(v));
    }
  });
}

